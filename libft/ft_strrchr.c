#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int			i;

	i = ft_strlen(s);
	s = (s + i);
	while (*s && *s != c)
	{
		s--;
	}
	if (*s == c)
	{
		return ((char *)s);
	}
	return (0);
}
